#include <iostream>
using namespace std;

int main () {

    // Operasi Penjumlahan
    cout << 5 + 2 << endl;

    // Operasi Pengurangan
    cout << 5 - 2 << endl;

    // Operasi Perkalian
    cout << 5 * 2 << endl;

    // Operasi Pembagian
    cout << 5 / 2 << endl;

    // Operasi Modulus
    cout << 5 % 2 << endl;
}