package hisaki;

public class KucingAggoraClass {
    public KucingAggoraClass(){
        // Tanpa Constructor (no-arguments constructor))
    }

    String nama;
    int berat;

    public KucingAggoraClass(String nama, int berat){
        // parametized constructor
        this.nama = nama;
        this.berat = berat;
    }
}
